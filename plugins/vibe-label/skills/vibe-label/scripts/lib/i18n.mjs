const COPY = { en: EN, 'zh-CN': ZH_CN };

export function normalizeLocale(value) {
  const locale = String(value ?? '').trim().replaceAll('_', '-').toLowerCase();
  if (locale === 'en' || locale === 'en-us') return 'en';
  if (locale === 'zh' || locale === 'zh-cn' || locale === 'zh-hans' || locale === 'zh-hans-cn') return 'zh-CN';
  return null;
}

export function getCopy(value = DEFAULT_LOCALE) {
  return COPY[normalizeLocale(value) ?? DEFAULT_LOCALE];
}
